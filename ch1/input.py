
# Example 1: Taking a simple input string and printing the result
name = input("Enter your name: ")
print(f"Your name is {name}.")

# Example 2: Taking numerical input (integer) and printing the result
age = int(input("Enter your age: "))
print(f"Your age is {age}.")

# Example 3: Taking a float input and printing the result
height = float(input("Enter your height in meters: "))
print(f"Your height is {height} meters.")

# Example 4: Taking multiple inputs into a list and printing the result
numbers = input("Enter three numbers separated by spaces: ").split()
print(f"The numbers you entered are: {numbers}")

# Example 5: Taking input with a specific prompt and printing the result
city = input("Which city are you from? ")
print(f"You are from {city}.")
