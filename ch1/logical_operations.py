# Example 1: Logical AND
x = True
y = False
result_and = x and y
print(f"Logical AND: {x} and {y} = {result_and}")

# Example 2: Logical OR
x = True
y = False
result_or = x or y
print(f"Logical OR: {x} or {y} = {result_or}")

# Example 3: Logical NOT
x = True
result_not = not x
print(f"Logical NOT: not {x} = {result_not}")
