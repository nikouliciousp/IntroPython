# Python Naming Conventions Examples:

# 1. Variable Names: Use lowercase separated by underscores (snake_case)
user_name = "John Doe"  # Example of snake_case variable name

# 2. Constant Names: Use all uppercase letters with underscores
MAX_USERS = 100  # Example of an uppercase constant name

# 3. Function Names: Use snake_case for function names
def calculate_sum(a, b):
    return a + b  # Example of snake_case function name

# 4. Class Names: Use PascalCase for class names
class UserProfile:
    def __init__(self, name, age):
        self.name = name
        self.age = age  # Example of attributes using snake_case

