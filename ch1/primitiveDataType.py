# Example of an integer
integer_example = 42  # Integer (whole number)
print(f"Integer example: {integer_example}, Type: {type(integer_example)}")

# Example of a float
float_example = 3.14  # Floating-point (decimal number)
print(f"Float example: {float_example}, Type: {type(float_example)}")

# Example of a string
string_example = "Hello, World!"  # Textual data
print(f"String example: {string_example}, Type: {type(string_example)}")

# Example of a boolean
boolean_example = True  # Boolean (True or False)
print(f"Boolean example: {boolean_example}, Type: {type(boolean_example)}")

# Example of None (null equivalent)
none_example = None  # Represents the absence of a value or null
print(f"None example: {none_example}, Type: {type(none_example)}")

