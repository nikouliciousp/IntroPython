def type_examples():
    # Example with an integer
    num = 42
    print("Type of num:", type(num))

    # Example with a string
    txt = "Hello"
    print("Type of txt:", type(txt))

    # Example with a list
    lst = [1, 2, 3]
    print("Type of lst:", type(lst))

    # Example with a dictionary
    dct = {"key": "value"}
    print("Type of dct:", type(dct))

    # Example with a custom object
    class Sample:
        pass

    obj = Sample()
    print("Type of obj:", type(obj))

type_examples()
